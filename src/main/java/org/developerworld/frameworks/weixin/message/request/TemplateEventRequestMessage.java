package org.developerworld.frameworks.weixin.message.request;

/**
 * 模板信息响应事件
 * @author Roy Huang
 *
 */
public class TemplateEventRequestMessage extends EventRequestMessage {

	private long msgId;
	private String status;

	public long getMsgId() {
		return msgId;
	}

	public void setMsgId(long msgId) {
		this.msgId = msgId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return "TemplateEventRequestMessage [msgId=" + msgId + ", status=" + status + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + (int) (msgId ^ (msgId >>> 32));
		result = prime * result + ((status == null) ? 0 : status.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		TemplateEventRequestMessage other = (TemplateEventRequestMessage) obj;
		if (msgId != other.msgId)
			return false;
		if (status == null) {
			if (other.status != null)
				return false;
		} else if (!status.equals(other.status))
			return false;
		return true;
	}

}
